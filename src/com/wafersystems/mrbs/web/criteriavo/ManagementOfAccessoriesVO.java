package com.wafersystems.mrbs.web.criteriavo;

public class ManagementOfAccessoriesVO {
	
	/**
	 * 附件编号
	 */
	private String accessoriesId;
	
	/**
	 * 附件归属
	 */
	private String attachmentAttribution;
	
	/**
	 * 附件上传人
	 */
	private String attachmentUploadPerson;
	
	/**
	 * 附件类型
	 */
	private String accessoriesType;
	
	/**
	 * 附件名称
	 */
	private String accessoriesName;
	
	/**
	 * 手动输入方式
	 */
	private String manualInput;

	public String getAccessoriesId() {
		return accessoriesId;
	}

	public void setAccessoriesId(String accessoriesId) {
		this.accessoriesId = accessoriesId;
	}

	public String getAttachmentAttribution() {
		return attachmentAttribution;
	}

	public void setAttachmentAttribution(String attachmentAttribution) {
		this.attachmentAttribution = attachmentAttribution;
	}

	public String getAttachmentUploadPerson() {
		return attachmentUploadPerson;
	}

	public void setAttachmentUploadPerson(String attachmentUploadPerson) {
		this.attachmentUploadPerson = attachmentUploadPerson;
	}

	public String getAccessoriesType() {
		return accessoriesType;
	}

	public void setAccessoriesType(String accessoriesType) {
		this.accessoriesType = accessoriesType;
	}

	public String getAccessoriesName() {
		return accessoriesName;
	}

	public void setAccessoriesName(String accessoriesName) {
		this.accessoriesName = accessoriesName;
	}

	public String getManualInput() {
		return manualInput;
	}

	public void setManualInput(String manualInput) {
		this.manualInput = manualInput;
	}
	
	
}
